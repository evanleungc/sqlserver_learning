--DateTime Functions
--1. ISDATE: Check whether the input is date, time or datetime. 1-Yes, 0-No
			--RETURN 0 For datetime2
SELECT ISDATE('A STRING')
SELECT ISDATE(GETDATE())
SELECT ISDATE('2016-01-01 22:22:22.167') --RETURN 1
SELECT ISDATE('2016-01-01 22:22:22.167234') --RETURN 0 For datetime2

--2. Day, Month, Year
SELECT Day('2016/01/01')
SELECT Day('01/01/2016')

--3. DATENAME: Return a part of the date
SELECT DATENAME(WEEKDAY, '2012-09-30 12:43:46')
SELECT DATENAME(YEAR, '2012-09-30 12:43:46')
SELECT DATENAME(MONTH, '2012-09-30 12:43:46')
SELECT DATENAME(DAY, '2012-09-30 12:43:46')

SELECT DATENAME(MONTH, '2012-09-30 12:43:46')	--RETURN ENGLISH CHARACTER
SELECT MONTH('2012-09-30 12:43:46')				--RETURN NUMBER